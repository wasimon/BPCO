<h1>L&rsquo;ESERCIZIO FISICO</h1>
<p>La progressiva riduzione dell&rsquo;attivit&agrave; fisica, per evitare lo sforzo e la mancanza di respiro, determina una ridotta capacit&agrave; di movimento. Si innesca cos&igrave; un circolo vizioso, da cui &egrave; difficile uscire. <br />
Per evitare che ciò accada, ecco un <strong>programma di esercizi</strong> appositamente pensato per la tua malattia, che oltre ad assicurare un migliore benessere, ti permette di valutare e misurare le tue capacit&agrave;.          </p>
<p><strong>I benefici dell&rsquo;esercizio fisico</strong></p>
<ul>
  <li>&egrave; divertente. Migliora il tono dell&rsquo;umore. &egrave; un espediente eccellente per controllare l&rsquo;ansia. </li>
  <li>Migliora la tua qualit&agrave; della vita. </li>
  <li>Ti consente una miglior conoscenza del tuo corpo e delle sue capacit&agrave; e ti aiuta a riconoscere i fattori che possono peggiorare i sintomi, permettendoti di tenerli sotto controllo. </li>
  <li>Migliora i tuoi movimenti, aumenta la resistenza e la tolleranza allo sforzo. Imparerai ad utilizzare al meglio le tue energie.<strong> </strong></li>
  <li>Migliora la capacit&agrave; del tuo cuore di pompare sangue e di trasportare ossigeno in tutto il corpo.<strong> </strong></li>
  <li>Migliora la circolazione del sangue e facilita lo scambio di anidride carbonica e di ossigeno, riducendo la mancanza di respiro.</li>
  <li>Aumenta la massa muscolare e la potenza dei muscoli. Ti permetter&agrave; di essere pi&ugrave; indipendente.</li>
</ul>
<p><strong>Quando</strong></p>
<ul>
  <li>La mattina e la sera sono i momenti pi&ugrave; indicati. Allenarsi in un luogo fresco &egrave; pi&ugrave; piacevole e meno faticoso.</li>
  <li>Aspetta almeno 2-3 ore dai pranzi. Non costringere il corpo a fare due cose nello stesso momento (esercitarsi e digerire).</li>
  <li>Se necessario, usa il broncodilatatore a breve durata d&rsquo;azione 20-45 minuti prima di iniziare. </li>
  <li>Controlla la mancanza di respiro durante gli esercizi.</li>
</ul>
<p><strong>Come</strong><strong> </strong></p>
<ul>
  <li>Indossa vestiti comodi e scarpe adatte. Un abbigliamento comodo mantiene la temperatura costante e ti permette di muoverti meglio e con maggior sicurezza.</li>
  <li>Se fa caldo, ricordati di bere in abbondanza sia prima che dopo l&rsquo;attivit&agrave; fisica. Non aspettare di avere sete. Bevi acqua per ristabilire la quantit&agrave; di liquidi persi durante l&rsquo;esercizio.</li>
  <li>Non avere fretta. Avrai pi&ugrave; benefici se esegui il tuo programma di esercizio con calma e in totale relax, magari ascoltando la tua musica preferita.</li>
  <li>Rispetta i tuoi limiti. Non devi esagerare ma divertirti.</li>
  <li>Usa le tecniche di respirazione. La respirazione a labbra strette riduce la mancanza di respiro.</li>
</ul>

