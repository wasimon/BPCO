		<h1> Esercizi di rinforzo per gli arti superiori</h1>
<ul>
  <li>Mentre fai gli esercizi, contrai i muscoli lentamente ma con forza. Ogni movimento muscolare deve durare 3-5 secondi.</li>
  <li>Esegui le indicazioni riportate nelle schede di ogni esercizio.</li>
  <li>Non devi provare dolore mentre esegui le ripetizioni</li>
  <li>Non dimenticare di praticare le tecniche di respirazione. Espira quando contrai i muscoli, inspira quando rilassi i muscoli.</li>
  <li>Allenati e fai gli esercizi di allungamento nella stessa stanza. Questo ti aiuter&agrave; ad evitare sbalzi di temperatura.</li>
</ul>

